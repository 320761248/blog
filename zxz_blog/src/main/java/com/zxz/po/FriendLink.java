package com.zxz.po;

public class FriendLink {
    private Integer id;
    private String friend_link_name;
    private String friend_link;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFriend_link_name() {
        return friend_link_name;
    }

    public void setFriend_link_name(String friend_link_name) {
        this.friend_link_name = friend_link_name;
    }

    public String getFriend_link() {
        return friend_link;
    }

    public void setFriend_link(String friend_link) {
        this.friend_link = friend_link;
    }
}
