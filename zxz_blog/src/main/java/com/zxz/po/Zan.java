package com.zxz.po;

public class Zan {

    private Integer id;
    private Integer zan_user;
    private String zan_time;
    private Integer comment;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getZan_user() {
        return zan_user;
    }

    public void setZan_user(Integer zan_user) {
        this.zan_user = zan_user;
    }

    public String getZan_time() {
        return zan_time;
    }

    public void setZan_time(String zan_time) {
        this.zan_time = zan_time;
    }

    public Integer getComment() {
        return comment;
    }

    public void setComment(Integer comment) {
        this.comment = comment;
    }
}
